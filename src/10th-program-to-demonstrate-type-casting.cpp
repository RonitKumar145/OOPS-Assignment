#include<iostream> 

using namespace std; 
int main(){
  //type casting 

  float DecimalNum = 10.5;

  int IntegerNum  = (int)DecimalNum;                    // conversion of float to integer (in type casting we can change the data type)

  cout<<"Decimal number: "<<DecimalNum<<endl;
  cout<<"After type casting to int: "<<IntegerNum<<endl;

  return 0;
}
