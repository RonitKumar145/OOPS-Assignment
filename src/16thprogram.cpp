#include<iostream> 

using namespace std; 
int main(){
   int num = 100 ;

   cout<<"value : "<<num<<endl;                  // initial value

   cout<<"Pre increment (++number): "<<++num<<endl;   // pre increment number

   cout<<"Pre increment (number++): "<<num++<<endl;   // increment

   cout<<"Post increment : "<<num<<endl;              // value post increment 

   cout<<"Pre decrement (--number): "<<--num<<endl;   // pre decrement number

   cout<<"Pre decrement (number--): "<<num--<<endl;   // decremment

   cout<<"Post decrement : "<<num<<endl;              // value post decrement

   return 0;

}