#include<iostream> 

using namespace std; 
int main(){
    // assignment operators

  int num = 7;                                //basic assignment '=' 
  cout<<"value = "<<num<<endl;                       

  num += 4;
  cout<<"After number += 4: "<<num<<endl;      // add and assign

  num -= 4;
  cout<<"After number -= 4: "<<num<<endl;      // subtract and assign

  num *= 4;
  cout<<"After number *= 4: "<<num<<endl;      // multiply and assign

  num /= 4;
  cout<<"After number /= 4: "<<num<<endl;     // divide and assign

  num %= 4;
  cout<<"After number %= 4: "<<num<<endl;     // modulus and assign

 return 0;
}